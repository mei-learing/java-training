package chaines;

public class Chaines {
	public static void main(String[] args) {
		String alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
		System.out.println("Cette chaine est: " + alphabet);
		System.out.println("Sa longueur est: " + alphabet.length());
		System.out.println("Le caractère de l’indice 4 est: " + alphabet.charAt(4));
		System.out.println("Sa version en minuscules est: " + alphabet.toLowerCase());
		System.out.println("Cette chaine est toujours: " + alphabet);
	}
}
